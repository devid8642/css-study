// Number é o tipo numérico que engloba vários tipos de números

// Inteiros
console.log(`Números inteiros: ...${-2}, ${-1}, ${0}, ${1}, ${2}...`);

// Reais
console.log(`3.5 + 2/3 = ${3.5 + 2/3}`);

// NaN -> Not a number
console.log(NaN);

// Infinity -> representa o infinito
console.log(Infinity);