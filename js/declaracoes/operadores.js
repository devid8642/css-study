// JS suporta os principais operadores aritméticos conhecidos
/*
	+ -> adição
	- -> subtração
	* -> multiplicação
	/ -> divisão
	** -> exponencianção
	% -> módulo
	++ -> incremento
	-- -> decremento
*/

// JS também suporta os principais operadores de atribuição
/*
	+= -> x += y é equivalente a x = x + y
	-= -> segue-se da mesma forma
	*= -> segue-se da mesma forma
	/= -> segue-se da mesma forma
	%= -> segue-se da mesma forma
	**= -> segue-se da mesma forma
*/

// JS também suporta os principais operadores de comparação
/*
	== -> igual a
	=== -> mesmo valor e mesmo tipo
	!= -> diferente de
	!== -> valor diferente e tipo diferente
	> -> maior que
	< -> menor que
	>= -> maior ou igual
	<= -> menor ou igual
	? -> operador térnario
*/

// JS também suporta os principais operadores lógicos
/*
	&& -> e
	|| -> ou
	! -> negação
*/